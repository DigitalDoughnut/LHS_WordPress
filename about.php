    <h1 class="pb-3 freeStyleH1 text-center">About Me</h1>
    <img src="img/elfSlant.png" alt="Legacy Chronicles Books" class="float-right ml-5 my-4" width=220px data-toggle="tooltip" title="Lauren as an elf">
    <p>I suppose I should start with the basics: I’m married to a wonderful man who’s encouraged me to write.
       I’m based in Yorkshire, England, but like to spend winters abroad, following the sunshine and becoming the
          seasonal envy of many friends. 
          I was an English teacher for sixteen years, 
          finishing as an Advanced Skills Teacher with an MA (Ed) in innovative teaching methods.
       I’m an imperfect Christian and have been all my life.</p>
    <p>There are more interesting details I could include, though: 
      I love to bake, host, create, and relax. 
      I can get so lost in a book that I forget to eat and don’t notice night falling. 
      I’ve often been found in semi-darkness, squinting at the pages in front of me and 
      surprised when someone turns on a light. 
      I love sci-fi and fantasy, history, and suspense but have been lured to the romance genre by my sister and mother.
       My nieces can also convince me to do nearly anything,
        including playing Minecraft with them most weekends (which I secretly love).
         At the point of writing this, I haven’t been to the ‘end’ yet,
       but I’m promised we’ll go soon and that there’ll be a dragon.</p>
       <img src="img/LaurenTeen.png" alt="Lauren as a teenager" class="float-left mr-5 my-4" width=220px data-toggle="tooltip" title="Lauren as a teenager">
    <p>I love claiming to be merely a retired teacher when travelling and meeting new people.
       It makes me feel part of a group of people who have worked hard and are now free to stop and look around
        – to enjoy life. For me, it’s a good reminder to relax and put my pen down once in a while.
         Besides, the faces people make while trying to figure out my age based on that information
          are often priceless.</p>
          
    <p>I make my own Christmas cards, tags, and presents where possible.
        There is something deeply satisfying about giving something I’ve designed and made,
        or cooked, to my husband and wider family.
        Watching their reactions is a special kind of fun that I wouldn’t want to miss.
        I write for pure joy but get an even bigger kick out of people reading and appreciating my work.
        I want to spend more time passing on the blessings I have to others,
        and I want to follow God’s plan for my life but sometimes forget to listen to his lead.</p>
        <img src="img/laurenyoung.png" alt="Lauren as a child" class="float-right ml-5 my-3" width=220px data-toggle="tooltip" title="Lauren as a child">
    <p>Whether you prefer simple facts or random nuggets,
       I guess you now know enough about me to see the type of person I am.
       I’ll leave you with one thing that sums up my character: my philosophy on life.</p>
    <p>Trust God, whatever happens; be helpful where possible; try not to worry, and have some fun each day.</p>
